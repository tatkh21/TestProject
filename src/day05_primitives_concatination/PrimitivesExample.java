package day05_primitives_concatination;

public class PrimitivesExample {
    public static void main (String[]args){
        // **** WHOLE NUMBERS/INTEGER TYPE****
        byte byteValue = 100;
        short shortValue = 3000;
        int intValue = 55000;
        long longValue = 2349503494L;
        // ****DECIMAL NUMBERS/FLOATING POINT TYPES****
        float floatValue = 5.55F;
        double doubleValue = 34567.7895;//we can put D at the end -> 23456.5674D
        //**** CHARACTER TYPE ****
        char charValue = 'A'; // only one character
        char ch = 'T';
        char lastLeterOfMyName = 'A';
        // **** BOOLEAN - true|false ****
        boolean booleanValue = true;
        // PRINT VARIABLE VALUES
        System.out.println (byteValue);
        System.out.println (shortValue);
        System.out.println (intValue);
        System.out.println (longValue);
        System.out.println (floatValue);
        System.out.println (doubleValue);
        System.out.println (charValue);
        System.out.println (booleanValue);


        System.out.println();
    }
}
