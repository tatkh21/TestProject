package day13_conditional_statments;

public class NiceDayOrNot {
    public static void main(String[] args) {
        int temp = 72;

        if (temp >= 70) {
            System.out.println("It is a nice day! Let's code Java");
        } else {
            System.out.println("It is kind of cold today!\n Let's code Java");
        }
    }
}